def add(a,b) :
    '''
    adding function
    a and b are the inputs to be given to perform add function
    example : add(3,2) output is 5
    '''
    return a+b

def multiply(a,b):
    '''
    multiplication function
    a and b are the inputs to be given to perform add function
    example : add(3,2) output is 6
    '''
    return a*b

def substract(a,b):
    '''
    substraction function
    a and b are the inputs to be given to perform add function
    example : add(3,2) output is 1
    '''
    return a-b